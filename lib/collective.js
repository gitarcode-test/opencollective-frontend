export const suggestSlug = value => {
  const slugOptions = {
    replacement: '-',
    lower: true,
    strict: true,
  };

  const truncateSlugToAllowedLimit = truncate(slugify(value, slugOptions), {
    omission: '',
    length: 30,
  });

  return trim(truncateSlugToAllowedLimit, '-');
};

